PROGRAM driver
	implicit none

	INTEGER N_OPTIONS, i

	INTEGER, DIMENSION(5) :: OPTIONS
	CHARACTER(LEN=20) PROBLEM_NAME

	N_OPTIONS = 3
	

	PROBLEM_NAME = ""
	
	DO I=1,N_OPTIONS
		OPTIONS(I) = 0
	END DO
	
	CALL UPDATE_CONFIG(OPTIONS, PROBLEM_NAME, N_OPTIONS)
	PROBLEM_NAME = "onephase"

	CALL READ_DENSE(PROBLEM_NAME, OPTIONS(3))



END
