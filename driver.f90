PROGRAM driver

	USE ALG_MATH
	USE MATRIX_IO
	
	implicit none

	INTEGER N_OPTIONS, i


	INTEGER, DIMENSION(5) :: OPTIONS
	CHARACTER(LEN=20) PROBLEM_NAME
	LOGICAL MATRIXFILE_EXISTS
	REAL, ALLOCATABLE :: A_ARR(:)

	TYPE(DENSE_MATRIX) :: A_MTX

	
	PROBLEM_NAME = ""
	
	DO I=1,N_OPTIONS
		OPTIONS(I) = 0
	END DO
	
	CALL UPDATE_CONFIG(OPTIONS, PROBLEM_NAME, N_OPTIONS)
	PROBLEM_NAME = "twophase"


	CALL READ_DENSE(PROBLEM_NAME, OPTIONS(3), A_MTX)

	
	WRITE(*, *) "Finished reading matrix of size ", &
	A_MTX%A_HEIGHT, A_MTX%A_WIDTH

END
