PROGRAM driver
	implicit none

	INTEGER N_COMMANDS, i

	INTEGER, DIMENSION(2) :: COMMANDS
	INTEGER, DIMENSION(2) :: OPTIONS
	CHARACTER(LEN=20) MATRIXFILE

	N_COMMANDS = 2
	

	MATRIXFILE = "Stranke94"
	
	DO I=1,N_COMMANDS
		COMMANDS(I) = I
		OPTIONS(I) = 0
	END DO
	
	CALL UPDATE_CONFIG(COMMANDS, OPTIONS, MATRIXFILE, N_COMMANDS)


	WRITE(*, '(A)') "Running with comfiguration:"


	DO I=1,N_COMMANDS
		WRITE(*, '(I2,A,I2)') COMMANDS(I), &
		 "=", OPTIONS(I)

	END DO


END
